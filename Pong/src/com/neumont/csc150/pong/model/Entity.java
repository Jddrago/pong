package com.neumont.csc150.pong.model;

public class Entity {
	
	private int x;
	private int y;
	private int height;
	private int width;
	private int deltaX;
	private int deltaY;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getDeltaX() {
		return deltaX;
	}
	public void setDeltaX(int deltax) {
		this.deltaX = deltax;
	}
	public int getDeltaY() {
		return deltaY;
	}
	public void setDeltaY(int deltay) {
		this.deltaY = deltay;
	}
	
	
	
}
